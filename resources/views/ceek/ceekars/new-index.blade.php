        <p>CEEKARS&trade; is the first smart audio headphone that combines haptic feedback and proprietary physics-based 3D Audio technology resulting in a degree of depth, interactivity and rich engulfing soundscapes.</p>
        <p>Traditional headphones are designed for predefined scenarios, not real-time dynamic lifelike experiences; therefore they do not react to movement or give indication of distance or direction. You can't really place where the sound originated i.e. up or down, or in front of you. Plus you cant feel the rumbling of a train or shuttle - with CEEKARS™ you can finally experience sound the way you would at the best movie theater or amusement park.</p>
