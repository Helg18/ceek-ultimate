      <h3 class="title2">Why Ceekars?</h3>
      <p>When we created the CEEK Virtual Reality content hub, we discovered that most people were using standard headphones for VR and missing a big part of the experience.  In many ways; using standard headphones for VR is like watching a 3D movie without the glasses - the experience is simply not complete!  Lo and behold…  The World's First VR Audio Headphone was born!</p>
