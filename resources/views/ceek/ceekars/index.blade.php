    <div class="inn">
      <h3 class="title2">Why Ceekars?</h3>
      <p>When we created the CEEK Virtual Reality content hub, we discovered that most people were using standard headphones for VR and missing a big part of the experience.  In many ways; using standard headphones for VR is like watching a 3D movie without the glasses - the experience is simply not complete!  Lo and behold…  The World's First VR Audio Headphone was born!</p>
      <p>We also found no portable or simple navigational tool for interacting with VR content, so we created a portable controller to pair with the headphones, creating a complete headset for everyone to enjoy and truly experience virtual reality - be it games, movies, concerts, sports and a variety of content in an exciting new way!  Thanks to the CEEK VR Audio Headset, you don’t just see content, you can be a part of it! </p>
